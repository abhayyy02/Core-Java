// Pre-Increment operator in java.

class Demo6
{
	public static void main(String[] args) 
	{
		int a = 2;
		int b = ++a;
		int c = 4;

		System.out.println(b);
		System.out.println(a);
		


		int d = 1;
		int e = 9;
		int f = 5;

		System.out.println(e++);
		System.out.println(a + f++);
		System.out.println(++c + d + e++);
	}
}